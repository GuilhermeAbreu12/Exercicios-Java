/* Contagem até 10: Use um laço for para imprimir os números de 1 a 10. */
package level3_lacos_repeticao;
public class Ex016_Contar1a10 {
    public static void main(String[] args){
        int i;
        System.out.println("\n============== Contagem até 10 ==============");
        System.out.println("De 1 ao 10, um de cada vez\n");
        for (i = 1; i<=10; i++)
            System.out.println(i);       
    }
}
